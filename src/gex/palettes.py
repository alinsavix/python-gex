"""IRGB color type and all Gauntlet II palette data."""

from __future__ import annotations


class IRGB:
    """Atari IRGB color: 0xIRGB where I=intensity, R/G/B are 4-bit channels."""
    __slots__ = ('irgb',)

    def __init__(self, irgb: int) -> None:
        self.irgb = irgb

    def to_rgba(self) -> tuple[int, int, int, int]:
        i = (self.irgb & 0xF000) >> 12
        r = ((self.irgb & 0x0F00) >> 8) * i
        g = ((self.irgb & 0x00F0) >> 4) * i
        b = (self.irgb & 0x000F) * i
        # Values range 0-225 (15*15); matches Go's NRGBA 8-bit storage
        return (r, g, b, 255)

    def __repr__(self) -> str:
        return f"IRGB(0x{self.irgb:04X})"


# Type alias for a palette (list of 16 IRGB colors)
Palette = list[IRGB]


def _p(*vals: int) -> Palette:
    """Shorthand to build a palette from hex values."""
    return [IRGB(v) for v in vals]


