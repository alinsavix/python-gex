def palette_clone(dest: Palette, src: Palette) -> None:
    dest[:] = src[:]


def palette_make_special(
    floorpattern: int, floorcolor: int, wallpattern: int, wallcolor: int
) -> None:
    palette_clone(TRAP_PALETTE[0], FLOOR_PALETTES[floorcolor])
    TRAP_PALETTE[0][S_COLORS_1[floorpattern]] = IRGB(0xA0AA)
    TRAP_PALETTE[0][S_COLORS_2[floorpattern]] = IRGB(0xA0AA)

    palette_clone(STUN_PALETTE[0], FLOOR_PALETTES[floorcolor])
    STUN_PALETTE[0][S_COLORS_1[floorpattern]] = IRGB(0xAAA0)
    STUN_PALETTE[0][S_COLORS_2[floorpattern]] = IRGB(0xAAA0)

    palette_clone(FORCEFIELD_PALETTE[0], FLOOR_PALETTES[floorcolor])
    FORCEFIELD_PALETTE[0][S_COLORS_1[floorpattern]] = IRGB(0xAA00)
    FORCEFIELD_PALETTE[0][S_COLORS_2[floorpattern]] = IRGB(0xDA60)

    SECRET_PALETTE[0][:] = [
        IRGB((c.irgb & 0x0FFF) + (min((c.irgb >> 12) + 4, 0xF) << 12))
        for c in WALL_PALETTES[0]
    ]

    if wallpattern >= 6:
        if wallcolor:
            palette_clone(SHRUB_PALETTE[0], WALL_PALETTES[wallcolor - 1])

        z = 0
        if wallpattern >= 11:
            z = 3
        cn = SHRUB_FLOOR_COLOR_NUMS[floorpattern]
        SHRUB_PALETTE[0][13:16] = [FLOOR_PALETTES[floorcolor][c] for c in cn[z:z + 3]]
